package com.myh.pojo;

public class Information {
    private Integer year;

    private Integer employmentRate;

    private Integer enrolmentRate;

    public Integer getYear() {
        return year;
    }

    public void setYear(Integer year) {
        this.year = year;
    }

    public Integer getEmploymentRate() {
        return employmentRate;
    }

    public void setEmploymentRate(Integer employmentRate) {
        this.employmentRate = employmentRate;
    }

    public Integer getEnrolmentRate() {
        return enrolmentRate;
    }

    public void setEnrolmentRate(Integer enrolmentRate) {
        this.enrolmentRate = enrolmentRate;
    }
}